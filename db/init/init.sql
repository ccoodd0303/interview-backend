119	3	동기(Synchronous)와 비동기(Asynchronous) 호출의 차이를 설명하세요.	동기(Synchronous) 호출 구조는 호출자 A가 피호출자 B를 호출했을 때, B의 작업이 완료되어 최종 리턴 값을 돌려줄 때까지 호출자가 작업 완료 여부를 확인하거나 블로킹(Blocking) 상태로 기다렸다가 다음 라인 코드를 실행하는 수직적 흐름입니다. 반면 비동기(Asynchronous) 호출 방식은 B에게 작업을 맡김과 동시에 호출자는 신경을 끊고 즉시 다음 코드로 넘어가 자기 할 일을 진행하며, 피호출자의 작업 완료 소식은 콜백(Callback) 핸들러나 이벤트를 통해 따로 통지받아 처리하는 차이가 있습니다.	3
120	3	자바의 스레드 스케줄링 방식을 설명하세요.	자바 어플리케이션 내부에서 돌아가는 멀티스레드들은 하드웨어 CPU 자원을 선점하기 위해 JVM 스케줄러의 통제 규칙에 따라 춤을 춥니다. 자바는 스레드 코드 단에서 부여한 숫자가 높을수록 CPU 영접 기회를 더 자주 배정해주는 우선순위(Priority) 방식 스케줄링과, 동일 선상에 놓인 스레드들에게 정해진 타임 슬라이스 시간 조각만큼 공평하게 CPU 자원을 쪼개어 번갈아 할당 제어하는 순환 할당(Round-Robin, 라운드 로빈) 방식을 결합 및 혼용한 아키텍처에 따라 전체 실행 흐름이 정밀하게 조율됩니다.	3
121	4	프로세스와 스레드의 차이를 설명해주세요.	운영체제로부터 메모리와 CPU 자원을 독립 할당받아 독자적인 주소 공간(Code, Data, Stack, Heap) 단위로 실행되는 격리 주체인 프로세스와 달리, 스레드는 하나의 프로세스 울타리 내부에서 태어나 코드, 데이터, 힙 영역 자원을 다른 스레드와 철저히 공유(자원 공유)한 채 오직 자신만의 독립적인 스택 메모리 공간만 들고 가볍게 실행되는 경량화 실행 단위라는 거대한 차이가 있습니다.	3
122	4	교착상태(Deadlock)의 발생 조건 4가지를 설명해주세요.	시스템이 데드락 교착상태 늪에 완전히 빠져버리기 위해선 4가지 조건이 수학적으로 동시 만족해야 합니다. 자원을 오직 한 프로세스만 독점해야 한다는 상호 배제(Mutual Exclusion), 자원을 할당받은 상태에서 타 자원을 요구하며 대기하는 점유 대기(Hold and Wait), 남이 쥔 자원을 강제로 뺏어올 수 없다는 비선점(No Preemption), 그리고 프로세스들이 서로 원형 구조로 락이 풀리기만을 고대한다는 순환 대기(Circular Wait) 조건이 바로 그것입니다.	3
123	4	페이징(Paging)과 세그멘테이션(Segmentation)의 차이는 무엇인가요?	두 기술은 가상 메모리 관리의 핵심 분할 기법입니다. 페이징(Paging)은 물리 메모리와 가상 주소 공간을 컴퓨터 관점에서 오직 기계적으로 쪼갠 동일한 고정 크기 블록(페이지)으로 나누어 관리하여 외부 단편화 문제를 해결하는 대신 내부 단편화 문제를 낳습니다. 반면 세그멘테이션(Segmentation)은 코드, 데이터, 스택 등 인간의 논리 구조 단위인 가변 크기 블록으로 메모리를 가르는 기법으로 내부 단편화는 없으나 외부 단편화 복잡도를 낳는 메커니즘 차이가 있습니다.	3
124	4	CPU 스케줄링 알고리즘의 종류를 설명해주세요.	CPU를 선점하려는 프로세스들을 지휘하는 스케줄링은 크게 둘로 나뉩니다. 먼저 한 번 CPU를 쥐면 절대 놓지 않는 비선점 방식에는 들어온 순서대로 처리하는 FCFS, 가장 짧은 작업 먼저 처리하는 SJF 등이 속하며, 실행 도중 자원을 강제로 회수해올 수 있는 강력한 선점 방식에는 정해진 타임 슬라이스(Time Slice)만큼 프로세스들에게 CPU 기회를 공평하게 배정하고 순환하는 라운드 로빈(Round Robin) 스케줄링 등이 활약합니다.	3
125	4	컨텍스트 스위칭(Context Switching)이란 무엇인가요?	다중 작업 운영체제 환경에서 CPU 사용권이 현재 돌던 A 프로세스로부터 차례를 맞이한 B 프로세스로 전환될 때 터지는 핵심 제어 메커니즘입니다. CPU 내부 하드웨어 레지스터들의 현재 적재값 등 A의 핵심 실행 상태 생존 정보들을 전용 메모리 블록인 PCB(Process Control Block) 공간에 상태 저장 조치를 취한 뒤, 새롭게 CPU를 영접할 B 프로세스의 과거 저장된 PCB 상태 정보를 커널로부터 찾아와 하드웨어 레지스터로 복구(Context 복원)해주는 수반 작업을 뜻합니다.	3
126	4	가상 메모리(Virtual Memory)의 개념을 설명해주세요.	가상 메모리는 프로세스 전체가 비싼 물리 메모리(RAM) 위에 다 올라가지 않더라도, 당장 실행에 필요한 쪼가리 메모리 블록만 RAM에 올리는 요구 페이징 기법 등을 기반으로 작동하는 주소 공간 확장 기술입니다. CPU가 바라보는 가상 주소를 하드웨어 부품인 MMU(메모리 관리 장치)가 바쁘게 실제 RAM 주소로 매핑 변환해주며, 부족한 RAM 용량 한계를 극복하기 위해 보조기억장치(디스크) 일부를 마치 진짜 메모리인 양 사기 쳐 활용하는 고도의 운영체제 설계 공학입니다.	3
127	4	인터럽트(Interrupt)와 시스템 콜(System Call)의 차이는 무엇인가요?	인터럽트는 키보드 타이핑, 타이머 알람, 하드웨어 에러 등 시스템 외부 장치로부터 불시에 날아오는 하드웨어 발생 신호(하드웨어 인터럽트)가 주를 이루며 CPU의 고삐를 강제로 뺏어 커널 모드 전환을 유도합니다. 반면 시스템 콜은 사용자 영역에서 놀던 일반 프로세스가 파일 쓰기나 네트워크 소켓 열기 등 고권한의 커널 자원을 합법적으로 쓰기 위해 소스 코드 단에서 의도적으로 날리는 소프트웨어적인 권한 대행 요청 인터럽트라는 명확한 출처 차이가 있습니다.	3
128	4	캐시 메모리의 지역성 원리를 설명해주세요.	CPU와 느려터진 메인 RAM 사이에서 버퍼 역할을 수행하는 캐시 적중률(Cache Hit)을 극상으로 끌어올리기 위한 원리입니다. 방금 한 번 접근해서 실행한 코드나 변수 주소 자리는 가까운 미래에 또다시 찾을 확률이 매우 높다는 시간 지역성(Temporal Locality) 속성과, 이번에 읽어들인 메모리 주소와 물리적으로 바로 인접해 있는 이웃 사촌 공간의 데이터들을 연달아 긁어다 쓸 확률이 농후하다는 공간 지역성(Spatial Locality) 특성을 기반으로 설계되어 있습니다.	3
129	4	뮤텍스(Mutex)와 세마포어(Semaphore)의 차이를 설명해주세요.	뮤텍스(Mutex)는 오직 단 하나의 스레드나 프로세스만 자원에 들어오도록 자물쇠 열쇠를 단 1개만 쥐어주며 락을 건 소유주 스레드만이 락을 풀 수 있는(Key 소유주만 해제 가능) 상호 배제 장치인 반면, 세마포어(Semaphore)는 가용한 공유 자원 개수 정보를 담은 카운터 변수 값을 지니고 있어 설정한 카운팅 개수만큼 다수의 스레드 진입을 동시 허용할 수 있으며(동시 진입 개수 제어) 소유주가 아닌 다른 스레드도 시그널을 날려 락 해제를 도울 수 있는 유연함의 차이가 있습니다.	3
130	4	프로세스 동기화가 필요한 이유를 설명해주세요.	동일 자원을 공유하는 다중 프로세스 레이어 환경에서, 둘 이상의 프로세스들이 공유 자원 공간에 동시 접근하여 값을 덮어쓰려고 피 튀기게 경쟁하는 레이스 컨디션(경쟁 상태)을 완벽히 통제해야 하기 때문입니다. 오직 한 놈만 안전하게 들어가야 하는 성역인 임계 영역(Critical Section) 내부를 완벽하게 순차 제어 조치함으로써, 훼손되기 쉬운 데이터 일관성 및 정합성을 빈틈없이 수호하기 위해 절대적으로 필요합니다.	3
131	4	대칭키 암호화와 공개키(비대칭키) 암호화의 차이점을 설명하세요.	대칭키 암호화는 데이터를 잠글 때(암호화)와 열 때(복호화) 완벽히 같은 키(동일한 키 사용)를 공유하므로 알고리즘이 가볍고 연산 속도가 무척 빠르나 키 배송 과정에서 키 탈취 위협에 노출되는 약점이 있습니다. 반면 공개키 암호화는 모두에게 까발리는 공개키와 자신만 꽁꽁 숨기는 비밀키라는 서로 다른 키 한 쌍(서로 다른 키 사용)을 비대칭 형태로 운영하여 키 유출 우려가 없고 배송이 안전하지만, 수학적 고차원 연산 오버헤드 때문에 속도 면에서 상대적으로 무겁고 느려진다는 차이가 있습니다.	3
132	4	OSI 7계층 모델의 구조와 데이터 단위(PDU)를 설명하세요.	네트워크 통신망을 체계화한 OSI 7계층 구조는 하위 물리-데이터링크-네트워크-전송 계층을 지나 상위 세션-표현-응용 계층의 7층 빌딩 구조로 구성됩니다. 각 층마다 통신을 주고받는 프로토콜 데이터 단위(PDU) 규격 명칭이 다른데, 2층 데이터링크단에선 프레임(Frame), 3층 라우팅 네트워크단에선 패킷(Packet), 4층 신뢰성의 전송 레이어단에선 데이터 조각을 뜻하는 세그먼트(Segment) 단위를 사용하여 통신을 수행합니다.	3
133	4	TCP와 UDP의 결정적인 특성 차이를 설명하세요.	TCP는 장치 간 통신 전에 반드시 3-way handshake를 통해 가상 연결선 확립 단계를 거치며, 데이터 유실 시 재전송을 박는 흐름 제어 및 혼잡 제어 기능을 완벽 탑재하여 연결 지향적이고 높은 신뢰성(신뢰성 보장)을 보장하는 프로토콜입니다. 반면 UDP는 연결 수립 단계 따위 과감히 생략하고 일방적으로 데이터를 목적지 향해 쏘아 보내는 비연결성 특징을 지니며, 신뢰성은 없지만 헤더 오버헤드가 극도로 적고 전송 속도가 고속(고속 전송 가능)이라 실시간 스트리밍에 적합합니다.	3
134	4	HTTP와 HTTPS의 보안 매커니즘 차이를 설명하세요.	일반 HTTP 통신은 평문 텍스트 형태로 데이터를 주고받아 패킷 스니핑 가로채기 공격에 Vulnerable(취약)합니다. 이를 보완한 HTTPS는 기본 443 포트를 기본 대역으로 활용하며, 전송 레이어 단에 SSL/TLS 보안 소켓 계층을 덧씌워 통신 양 주체 간에 세션 대칭키를 안전하게 교환한 뒤, 오가는 모든 데이터 패킷 내용물을 철저하게 암호화(데이터 암호화 전송) 처리하여 개인정보를 철통 방어하는 메커니즘 차이가 있습니다.	3
135	4	DNS(Domain Name System)의 질의 프로세스를 설명하세요.	인간이 치는 영어 주소창 문자를 컴퓨터 전용 IP 주소으로 변환해주는 프로세스입니다. 브라우저가 로컬 캐시를 먼저 뒤진 뒤, 매칭 정보가 없으면 통신사 DNS 담당자인 리커시브(Recursive) 서버에게 요청을 토스합니다. 질의를 위임받은 리커시브 서버는 전 세계 네임서버의 대부인 루트(Root) 네임서버를 필두로 .com 같은 TLD 서버를 거쳐 최종 권한 네임서버(Authoritative Name Server)까지 재귀적/반복적 쿼리를 던져 최종 실제 IP 반환 값을 받아와 리턴해 줍니다.	3
