140	4	DDoS(분산 서비스 거부 공격)의 개념을 설명하세요.	DDoS 공격은 해커가 사전에 악성코드로 전 세계 사용자 컴퓨터들을 감염시켜 원격 조종이 가능한 수많은 좀비 PC 부대 네트워크인 봇넷(Botnet)을 구축하는 데서 시작합니다. 이후 특정 타겟 시스템의 웹 서버 주소를 향해 이 좀비 PC들을 일시에 원격 가동하여 감당하기 힘든 과도한 대량 트래픽 융단폭격을 무차별 주입 및 발생시킴으로써 하드웨어 자원을 고갈시키고 시스템을 마비 지경으로 몰고 가는 분산 서비스 거부 공격입니다.	3
