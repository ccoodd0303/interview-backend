51	2	데이터 무결성(Integrity)의 종류를 설명하세요.	데이터베이스 정합성을 보장하는 무결성에는 테이블의 기본키는 절대 NULL이나 중복이 될 수 없다는 개체 무결성, 외래키 값은 참조하는 상위 부모 테이블의 기본키 값과 항상 일치하거나 NULL이어야 한다는 참조 무결성, 필드의 속성 값들이 지정된 데이터 타입 및 정해진 조건 범위 내에 존재해야 한다는 도메인 무결성 등이 있습니다.	3
52	2	기본키(Primary Key)와 외래키(Foreign Key)의 역할을 설명하세요.	기본키(PK)는 테이블 내의 수많은 레코드(행) 중 오직 하나의 행만을 유일하게 식별할 수 있도록 도와주는 고유 식별자(Unique, Not Null) 역할을 수행하며, 외래키(FK)는 두 테이블 간의 참조 관계를 맺어줌으로써 부모-자식 테이블 간 데이터 무결성과 일관성을 강력하게 묶어주는 연결고리 역할을 담당합니다.	3
53	2	트랜잭션 격리 수준(Isolation Level)의 종류를 말해주세요.	트랜잭션 격리 수준의 종류에는 커밋되지 않은 타 트랜잭션의 변경 데이터를 읽는 Read Uncommitted, 커밋이 완료된 데이터만 읽도록 보장하는 Read Committed, 한 트랜잭션 내에서 같은 조회 쿼리를 반복해도 항상 동일 결과가 나오는 Repeatable Read, 완전한 일렬 처리를 지원하여 동시성을 철저히 제어하는 Serializable이 있습니다.	3
54	2	Dirty Read 현상이란 무엇인가요?	Dirty Read(더티 리드) 현상이란 특정 트랜잭션 A 내부에서 수행 중인 데이터 삽입 및 수정 등의 작업이 아직 완료되지 않은 상태(미커밋 상태)임에도 불구하고, 다른 트랜잭션 B가 해당 변경 중인 불완전한 데이터를 중간에 그대로 읽어버렸다가 트랜잭션 A가 롤백될 때 데이터 정합성이 깨지는 격리성 훼손 현상입니다.	3
