143	4	방화벽(Firewall)과 IDS/IPS의 차이를 설명하세요.	네트워크 보안 장비 중 방화벽(Firewall)은 인프라 최외곽 레벨에서 오직 데이터 패킷의 출발지/목적지 IP 주소와 포트(Port) 번호 규격만을 단순 검사하여 접근 제어 규칙에 따라 유해 접근을 원천 차단하는 장비입니다. 반면 IDS(침입 탐지 시스템)는 패킷 내부의 실질적인 데이터 패턴을 정밀 분석하여 해킹 침입 탐지 시 경고 알람을 쏘아주며, 한 단계 더 진화한 IPS(침입 방지 시스템)는 탐지를 넘어 실시간으로 악성 패킷 선로 자체를 강제 드롭시키며 실시간 침입 차단 행위까지 지능적으로 수행하는 차이가 있습니다.	3
