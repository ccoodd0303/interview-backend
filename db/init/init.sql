89	3	트라이(Trie) 자료구조의 특징과 사용 사례를 설명해주세요.	트라이(Trie)는 일반적인 문자열 집합을 탐색할 때 탐색 성능을 극대화하기 위해 문자 한 글자 한 글자를 트리 형태의 노드로 계층 구조화하여 내려가는 특수한 형태의 문자열 탐색 트리 자료구조입니다. 문자열의 공통된 접두사(Prefix) 구조 정보를 공유하여 중복 저장을 막고 글자 매칭을 고속화하므로, 대형 포털 검색창의 검색어 자동 완성 시스템이나 철자 검사기 기능처럼 문자열을 고속 검색해내야 하는 유스케이스에서 독보적인 위상을 발휘합니다.	3
90	3	시간복잡도 Big-O 표기법을 설명하고 예시를 들어주세요.	Big-O(빅오) 표기법이란 작성한 특정 알고리즘의 효율성을 평가할 때, 최선의 상황이 아닌 시스템이 마주할 수 있는 가장 최악의 상황(Worst Case) 시나리오 환경 아래에서 입력 데이터 규모 n이 무한히 커짐에 따라 실제 연산 실행 횟수와 걸리는 시간이 어떤 증가율 곡선을 그리며 늘어나는지 수학적 함수로 단순화하여 나타낸 점근적 표기 지표입니다. 예컨대 데이터 개수에 상관없이 상수를 타는 O(1), 데이터 규모만큼 루프를 도는 선형 시간 O(n), 균형 트리 분할을 타는 로그 시간 O(log n) 등이 있습니다.	3
91	3	자바의 가비지 컬렉션(GC)의 역할과 원리를 설명하세요.	자바의 가비지 컬렉션(GC)은 개발자가 명시적으로 메모리를 해제하지 않아도, JVM 내부 엔진이 동적 객체가 적재되는 공간인 힙(Heap) 메모리 영역을 수시로 감시하는 기법입니다. 어플리케이션 내의 어떤 유효 스레드 변수의 Root 공간으로부터도 참조 구조 링크가 도달하지 않는 유효하지 않은(Unreachable) 쓰레기 객체들을 식별하고, 이들이 쥐고 있던 메모리 자원을 자동으로 해제 및 수거해 줌으로써 개발자를 메모리 누수 지옥으로부터 구제하는 역할을 수행합니다.	3
92	3	자바의 JVM 구조와 메모리 영역(Runtime Data Area)을 설명하세요.	JVM은 운영체제 독립적으로 자바 바이트코드를 번역하고 구동하는 가상기관입니다. 내부 핵심 메모리 영역인 Runtime Data Area는 모든 스레드가 공유하며 클래스 구조와 static 변수가 올라가는 메서드 영역(Method Area), 동적으로 생성된 인스턴스 객체들이 살며 GC의 주 관리 대상이 되는 힙 영역(Heap), 그리고 각 스레드마다 독립 생성되어 메서드 호출 시마다 매개변수와 지역변수 프레임이 쌓였다가 휘발되는 JVM 스택 영역(Stack), PC 레지스터, 네이티브 메서드 스택 등으로 밀도 있게 구성되어 작동합니다.	3
93	3	추상 클래스(Abstract Class)와 인터페이스(Interface)의 차이는 무엇인가요?	자바에서 추상 클래스는 클래스 간의 밀접한 혈연적 상속 관계를 바탕으로 서브 클래스들의 공통 신체 구조나 상태 필드를 물려주고 기능을 확장(상속 관계)하는 구조이며 다중 상속 불가의 제약이 따릅니다. 반면 인터페이스는 혈통과 무관하게 아키텍처 상 클래스가 "어떤 명세 기능을 구현할 수 있는지" 행위 규약을 정의(행위 규약)하는 것이 주 목적으로, 몸체가 없는 순수한 추상 메서드들의 집합으로 이루어져 있으며 한 클래스가 여러 개의 규약을 동시 소화해내는 다중 구현을 유연하게 허용하는 사상적 차이를 보입니다.	3
94	3	오버로딩(Overloading)과 오버라이딩(Overriding)의 차이를 설명하세요.	오버로딩은 한 클래스 내부 혹은 상속 관계에서 이미 존재하는 메서드와 이름은 같고 받아들이는 매개변수의 타입이나 개수, 순서 규격을 다르게 정의하여 동일 이름의 메서드 시그니처를 여러 개 확장(정적 바인딩)하는 컴파일 시점의 기술입니다. 반면 오버라이딩은 부모 클래스가 이미 정의해 둔 메서드를 자식 클래스가 자신의 성격에 맞게 메서드 시그니처(이름, 매개변수, 반환타입)를 똑같이 유지한 채 내부 실행 바디만 새롭게 고쳐 쓰는 재정의(동적 바인딩) 개념으로, 프로그램 실행 중에 실제 인스턴스 타입에 맞는 메서드가 역동적으로 찾아져 돌아가는 다형성의 핵심 메커니즘입니다.	3
95	3	자바의 Call by Value와 Call by Reference의 동작을 설명하세요.	자바는 메서드로 인자를 넘길 때 내부적으로 절대 Call by Reference 방식을 쓰지 않고, 오직 변수가 들고 있는 실제 비트 값을 그대로 복제해서 던지는 Call by Value 방식으로만 일관되게 동작합니다. 기본형 변수는 담긴 리터럴 값 복사가 일어나고, 참조형 객체 변수를 인자로 넘길 때 역시 객체가 살고 있는 메모리 주소를 가리키는 참조 주소값(주소값 복사) 복사가 정밀하게 발생하여 전달되므로, 메서드 내부에서 넘겨받은 파라미터 변수 자체에 새 객체를 갈아 끼워봤자 원본 참조 변수에는 일절 아무 영향도 주지 못합니다.	3
96	3	Equals()와 == 연산자의 차이를 설명하세요.	자바에서 `==` 비교 연산자는 기본형 변수일 때는 들고 있는 값을 직접 대조하고, 참조형 객체 변수일 때는 비교 대상 두 객체가 메모리 상의 완벽히 동일한 주소에 안착해 있는지 판별하는 주소 비교(동일성, Identity) 연산자로 쓰입니다. 반면 `equals()` 메서드는 주소지가 설령 다를지라도 두 객체가 품고 있는 속성 내용물들이 논리적인 관점에서 실질적으로 같은 가치를 지니고 있는지 판별하는 값 비교(동등성, Equality) 목적으로 설계된 메서드입니다. 때문에 문자열이나 비즈니스 객체 내용 비교 시에는 `equals()` 커스텀 오버라이딩 구현과 `hashCode()` 동시 재정의를 필수로 동반해야 합니다.	3
97	3	String, StringBuffer, StringBuilder의 차이를 설명하세요.	자바에서 `String` 객체는 한 번 생성되면 메모리 내부 값이 절대 변하지 않는 아주 단단한 불변(Immutable) 성격을 지녀 잦은 문자열 더하기 연산 시 매번 새 객체가 생성되어 가비지가 양산되는 메모리 낭비를 가집니다. 이를 보완하기 위해 내부 문자 배열 가변 처리를 지원하는 두 가변 클래스가 존재하는데, `StringBuffer`는 메서드마다 동기화 키워드가 적용되어 멀티스레드 환경에서 안전한 Thread-safe 성격을 취하는 반면, `StringBuilder`는 동기화 제어 장치를 과감히 걷어내어 단일 스레드 환경 조건 아래에서 타의 추종을 불허하는 초고속 연산 처리 성능을 보장한다는 구조적 성격 차이가 뚜렷합니다.	3
98	3	자바의 컬렉션 프레임워크 중 List, Set, Map의 특징을 말해주세요.	컬렉션 프레임워크의 핵심 3대장 중 `List`는 데이터들의 들어온 순서 유지를 보장하며 인덱스를 통한 정밀 접근이 가능하고 데이터 중복 저장을 허용합니다. 반면 `Set`은 데이터 순서를 전혀 보장하지 않는 대신 수학의 집합처럼 동일 데이터의 중복 저장 불가 제약을 지녀 고유 데이터 보존에 특화되어 있습니다. 마지막으로 `Map`은 단독 데이터가 아닌 고유 식별 명찰 역할을 수행하는 Key와 실제 알맹이 데이터인 Value가 단짝 쌍(Pair)으로 묶여 저장되는 Key-Value 구조를 취하며, Key 값은 중복될 수 없다는 고유 개성을 뽐냅니다.	3
99	3	스레드(Thread)를 자바에서 구현하는 두 가지 방법을 설명하세요.	자바에서 멀티스레드 실행 줄기를 구현하기 위한 첫 번째 코딩 방식은 언어가 제공하는 `Thread` 클래스를 직접 상속(extends)받아 내부의 `run()` 메서드를 오버라이딩 구현하는 방법입니다. 두 번째는 자바의 단일 상속 제한 제약을 영리하게 우회하기 위해, 스레드 실행 바디 명세만을 담고 있는 인터페이스인 `Runnable` 인터페이스를 장착 및 구현(implements)한 뒤, 이를 실제 스레드 인스턴스 생성자 파라미터로 넘겨주어 구동시키는 다중 상속 우회 구현 방식이 존재합니다.	3
100	3	자바의 예외 처리(Exception) 중 Checked와 Unchecked의 차이를 설명하세요.	Checked Exception은 `Exception` 클래스를 상속받는 계열로, 자바 소스 코드가 컴파일되는 시점에 컴파일러가 소스 상에 반드시 try-catch 구문으로 감싸거나 throws 선언을 했는지 체크하여 예외 처리를 필수 강제하는 안전망 성격입니다. 반면 Unchecked Exception은 `RuntimeException` 밑으로 자라나는 계열로, 컴파일 시점에는 체크하지 않다가 프로그램이 구동되는 와중(런타임)에 비로소 조건 오류나 null 터짐 등으로 돌발 출현하는 예외로 예외 처리가 문법적으로 강제되지 않는 자율성을 갖습니다.	3
101	3	함수형 프로그래밍과 람다식(Lambda Expression)의 장점을 설명하세요.	함수형 프로그래밍은 외부 데이터의 상태 변경을 배제하고 부작용(Side Effect)이 없는 순수 함수의 조합 파이프라인으로만 안정적인 로직을 흘려보내는 선언형 패러다임입니다. 자바는 이를 지원하기 위해 익명 함수 표현식인 람다식(Lambda Expression)을 지원하는데, 기존 함수형 인터페이스 구현을 위한 코드 어구들이 간결 화 형태로 비약적으로 압축되어 텍스트 라인이 줄어들고 소스 코드 자체의 가독성을 폭발적으로 끌어올리는 막강한 장점을 선사합니다.	3
